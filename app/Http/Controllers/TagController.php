    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $categories = Category::all();
        $tags = Tag::all();
        $selected_tag = Tag::find($id);
        if (!empty($selected_tag))
        {
            $articles = ArticleTag::where('tag_id', $id)->get();
            return View::make('tag.show')->with(array('categories' => $categories,
                        'tags_list' => $tags, 'articles' => $articles, 'selected_tag'
                        => $selected_tag));
        }
        else
        {
            return abort(404);
        }
    }

